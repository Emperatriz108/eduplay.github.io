    <h1>Implementacion Aplicacion Movil</h1>
    <h2>Fomente el aprendizaje de los niños con una aplicación móvil interactiva</h2>
</div>
<div id="inicio" class="s1">
<p>La educación es una parte fundamental para el desarrollo de una persona, es un proceso constante de adquisición de conocimientos 
    desde nuestros primeros años como aprender a leer, escribir y relacionar. Las Instituciones Educativas han utilizado recursos 
    como libros, cuadernos de ejercicios, tareas extracurriculares, actividades de aula, entre otros recursos para la enseñanza. 
    Sin embargo, algunos niños podrían tener dificultades para entender o comprender temáticas impartidas en clase. 
    A medida que pasa el tiempo las tecnologías han ido evolucionando y forman parte de nuestro día a día, es una herramienta que la 
    mayoría de las personas utilizan para diversas actividades o entretenimiento, entre esos usuarios son los niños que los usan 
    como medio de entretenimiento. A partir de esa situación se plantea la implementación de una aplicación móvil enfocada en la 
    Educación Inicial y Parvularia y Educación Básica para promover el aprendizaje interactivo distribuido en las diferentes 
    asignaturas que se presente según el nivel académico. 
<br><br>
Esta aplicación se enfocaría en realizar diversas actividades entretenidas e interactivas para los niños donde se presenta un 
recurso educativo de una materia expresado en un juego dinámico según la temática del recuso. Estos juegos o actividades van a 
ser ponderadas o basadas en la puntuación para analizar el rendimiento y proceso de aprendizaje del estudiante. 
<br><br>
El docente podrá ver el proceso del estudiante según el resultado de los datos de puntuación a través de su usuario correspondiente, 
estos datos brindaran una mejor idea del proceso de aprendizaje de los estudiantes y analizar en que temáticas podría reforzarse.
</p>

